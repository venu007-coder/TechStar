  The trading of derivatives,
largely privately negotiated and traded, is going through significant
transformation, in what constitutes the biggest financial regulatory revamp
since the 1930s. In recent years, derivatives have constituted huge profit
machines at institutions like Goldman Sachs, JPMorgan and Morgan Stanley.
Now proposals in Congress call for Wall Street firms to wall off their
derivatives businesses and place them in subsidiaries requiring
substantially more capital. Some are now declaring that derivatives trading
will be a less profitable endeavor over the next decade.

Glimmers are
emerging of a new financial landscape of lower profits and reduced risks,
even before Congress completes the financial regulation overhaul. Will
profits really suffer whatever final form of the law emerges? How banks and
brokerage firms will adjust to the changes and find ways to work around the
new regulations? Which new revenue streams can be thought of to make up for
reduced profits? How long will it take for Wall Street to adapt to the new
regulations and thrive again?  [11] Golden Networking [12]'s Derivatives
Leaders Forum 2010 [13] will provide attendees with answers to questions on
how to successfully navigate the new regulatory landscape for
over-the-counter and exchange-traded derivatives, devise profit-increasing
strategies to enhance the performance of trading desks and anticipate
developments in the industry.  

