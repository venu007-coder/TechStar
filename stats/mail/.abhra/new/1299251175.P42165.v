Business Impact Analysis (BIA) is the process of analyzing all core business
functions and establishing an optimized

timetable for recovery.
A BIA is a means of systematically assessing the potential impacts to the
business in the event that the system suffers a

catastrophic failure and is not available. The business impact analysis is
intended to help understand the degree of

potential loss (and various other unwanted effects) that could occur. This
will cover not just direct financial loss, but other

issues such as damage to reputation, regulatory effects and so on.
The BIA will identify and document the business processes associated to the
application, identify application(s), data, and

system(s) that support the process, and describe dependency relationships
between applications and/or data. It is critical

to determine the business effects of losing systems or not being able to
complete daily operations. Processes can be

reviewed and ranked.

