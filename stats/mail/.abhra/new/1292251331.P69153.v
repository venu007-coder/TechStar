Hi,

Title: Lead Siebel Functional Business Analyst
Location: Milwaukee, WI
Duration: 12 months+

Lead Siebel Functional Business Analyst

Responsibility Level:
Responsible for project/system development and support. Use knowledge
of business and technology trends to select and develop
project/systems strategies that are responsive to the needs of the
Controls Group's business units and customers. Establish and execute
project/systems schedules and service levels that are compatible with
the business needs and cost targets. Provide continuous support to
business units and customers throughout the life of systems and
contracts.. Aggressively lead efforts in productivity and quality
improvements.

Principal Duties:
1. Work with business owners to identify and prioritize business
requirements.
2. Identify functionality enhancements that lead to continuous
improvement.
3. Manage efforts leading to functionality enhancements.
4. Coordination with development and functional resources for design
review and approval.
5. Coordination with QA/Testing activities.
6. Organize and direct a team of employees dedicated to the
functional and technological definition of projects and systems.
7. Responsible for selecting, advocating, and following open industry
standards and Controls Group Technology Architecture.
8. Manage all aspects of project/systems Life Cycle including
requirement definition, planning, resource allocation, development,
make/buy/customize decisions, deployment and support strategy,
system/project discontinuation, migration, intellectual property
protection, as well as the preparation of all necessary documentation,
and technology transfer.
9. Control, monitor and report on progress and costs, identify
potential difficulties, and highlight decision points for all assigned
projects.
10. Foster an environment which encourages effective working
relationships with other functional groups within and outside of
Controls Group Information Technologies, such as other Programs,
Corporate Information Technologies, Product Engineering, Business and
Controls Group Management, Marketing, Support Services, Training,
vendors, consultants, etc.
11. Develop and execute quality programs, including defining, measuring,
and tracking both in-process and delivery measurements and metrics. Use
quality data and metrics to improve quality of Controls Group IT
processes and systems.
12. Provide technical support during the lifetime of the
projects/systems. Anticipate technology changes, and plan for the
evolution and/or replacement of the projects/systems.
13. Manage relationships and negotiations with outside vendors.
Authorize contracts with consultants and other outside vendors as
needed, within approved limits.
14. Provide comprehensive management reports on projects.


Qualifications

Requirements:

1. Bachelor of Science Degree in Engineering or Computer Science, or
equivalent experience.
2. Demonstrated technology/systems management experience, or
demonstrated project leadership, people leadership, and technology
leadership skills.
3. Knowledge of CRM concepts and processes (preferably Siebel driven)
4. Knowledge of Service businesses (Quote-to-Cash and Procure-to-Pay
processes).
5. Knowledge of SOX controls and how they affect day-to-day business
processes.
6. 24x7 availability for escalation and business process monitoring
(to provide business process prioritization and work-around
identification during critical outages).
7. Ability to travel to the branches and work with end users.
8. Five years of Systems or Technology Development/Operations
experience.
9. Knowledge of latest information technology tools and practices and
how to cost-effectively apply them to solve business problems.
10. Knowledge of procedures in relevant IT operations, technology,
and/or business functional areas such as marketing, product development
and support processes and procedures.
11. Demonstrated ability to influence others at multiple organizational
levels, to lead and work in a team-environment; ability to lead
collaborative efforts with user, development, business, and support
groups; good written and oral communication skills, flexibility and
adaptability to changing situations.
12. Ability to accurately judge individual capabilities and limitations,
to manage the performance and contributions of others, to provide
sensitive and constructive feedback, and to provide career development
coaching for technical professionals.



