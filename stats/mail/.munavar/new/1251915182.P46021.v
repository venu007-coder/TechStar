- Major trends that are accelerating mobile Internet market growth

- Identifying the key stakeholders in the mobile Internet ecosystem
and evaluating the likely winners and losers
- How participants build viable business models to profit from the
expansion of the mobile Internet

- The next five years of evolution of the mobile Internet

Like the impact of the fixed Internet, the extension of the Internet
to the world of mobility is bringing about change to both enterprises
and consumers. A broad range of businesses now need to fundamentally
rethink how they can take advantage of these major shifts in
communications and technology to make them more profitable and
competitive.

If you work in one of the many affected areas, you may want to gain a
broader contextual understanding of where your business and its
products and services fit into the overall ecosystem.  You may also
want to learn about market dynamics that may affect your business
planning, career or investment decisions.  Maybe you are new to the
mobile Internet, or are employed somewhere in the broader ecosystem
and may not feel comfortable with your knowledge of mobile Internet
market trends.	Or, perhaps you are simply interested in learning
about the dynamics shaping one of the fastest growing technology
markets today.	If any of these describe you then Harnessing the
Mobile Internet should be your next =E2=80=9Cmust read=E2=80=9D.

