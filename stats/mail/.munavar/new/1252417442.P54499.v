Tips for business continuity and contingency planning for swine flu
http://go.techtarget.com/r/9163044/6037497
[Linda Tucci, Senior News Writer]

With many students back to school this week, the federal government
and health organizations are doubling down on efforts to get out the
word on a second wave of the H1N1 virus, also known as swine flu. A
presidential advisory group of leading scientists has warned of
possible absentee rates of 30% to 50% for an unknown duration. So
what should CIOs be doing to their business continuity and
contingency plans to help prepare for a swine flu outbreak? 

A few basic guidelines should help you customize your business
continuity plan for this kind of disaster, in which absenteeism and
an uncertainty of duration are key factors. For this advice, we
conferred with Alan Berman, executive director at the Disaster
Recovery Institute International and former CIO for a major financial
institution, and two analysts specializing in business continuity and
disaster recovery: Stephanie Balaouras of Cambridge, Mass.-based
Forrester Research Inc., and Roberta Witty of Stamford, Conn.-based
Gartner Inc.

