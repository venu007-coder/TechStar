$(document).ready(function()
{
	$('.more').click(function()
	{
			$('.news-info').slideDown('slow');
			$('.moreclose').show();
			$(this).hide();
	});
$('.moreclose').click(function()
{
			$('.news-info').slideUp('slow');
			$('.more').show();
			$(this).hide();

});
});
