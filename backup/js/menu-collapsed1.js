$(document).ready(function()
{
	$('.more').click(function()
	{
			$('.more-info1').slideDown('slow');
			$('.moreclose').show();
			$(this).hide();
	});
$('.moreclose').click(function()
{
			$('.more-info1').slideUp('slow');
			$('.more').show();
			$(this).hide();

});
});
